import { Client, DEFAULT_CHECKLIST } from "@/types/client";

// Helper para criar checklist com progresso específico
const createChecklistWithProgress = (completedCount: number) => {
  const checklist = JSON.parse(JSON.stringify(DEFAULT_CHECKLIST));
  let count = 0;
  
  for (const section of checklist) {
    for (const item of section.items) {
      if (count < completedCount) {
        item.completed = true;
        item.completedAt = new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000).toISOString();
      }
      count++;
    }
  }
  
  return checklist;
};

